  RocketPickPic.prototype._showSelectionScreen = function (graphics) {
    var self = this;
    var $screen = $('<div class="rpp-select-screen"></div>');

    $('<div class="rpp-select-title"></div>')
      .text(this.params.l10n.chooseGraphic)
      .appendTo($screen);

    var $btns = $('<div class="rpp-select-buttons"></div>');
    graphics.forEach(function (g) {
      $('<button class="rpp-select-btn"></button>')
        .text(self.params.l10n[g.labelKey])
        .on('click', function () {
          self.currentGraphic = g;
          self._startGame();
        })
        .appendTo($btns);
    });

    $screen.append($btns);
    this.$container.html('').append($screen);
  };

  // ── Spiel starten ─────────────────────────────────────────────────────────

  RocketPickPic.prototype._startGame = function () {
    var self = this;
    this.correctCount  = 0;
    this.questionIndex = 0;
    this.gameOver      = false;
    this.seenItems     = [];

    this.questions = this.allQuestions.slice();
    if (this.params.shuffleQuestions) { this._shuffle(this.questions); }

    this.$container.html(this._buildShell());

    this.$svgWrap  = this.$container.find('.rpp-svg-wrap');
    this.$svg      = this.$container.find('.rpp-svg');
    this.$qtext    = this.$container.find('.rpp-question-text');
    this.$opts     = this.$container.find('.rpp-answer-options');
    this.$feedback = this.$container.find('.rpp-feedback');
    this.$progFill = this.$container.find('.rpp-progress-fill');
    this.$progTxt  = this.$container.find('.rpp-progress-text');
    this.$btn      = this.$container.find('.rpp-submit');

    this.$btn.on('click.rpp', function () { self._checkAnswer(); });
    this.$container.on('keydown.rpp', '.rpp-short-input', function (e) {
      if (e.key === 'Enter') { self._checkAnswer(); }
    });

    if (this.questions.length === 0) {
      this.$qtext.text('Keine Fragen gefunden. Bitte Fragen im Editor eingeben.');
      this.$btn.hide();
      return;
    }
    this._showQuestion();
  };

  // ── Hilfsfunktionen ──────────────────────────────────────────────────────

  RocketPickPic.prototype._getAvailableGraphics = function () {
    var g = this.params.graphics || {};
    var available = [];
    if (g.enableRocket    !== false) { available.push(GRAPHICS.rocket); }
    if (g.enableButterfly !== false) { available.push(GRAPHICS.butterfly); }
    if (g.enableOwl       !== false) { available.push(GRAPHICS.owl); }
    if (g.enableIcecream  !== false) { available.push(GRAPHICS.icecream); }
    return available;
  };

  RocketPickPic.prototype._parse = function (text) {
    var qs = [];
    (text || '').split('\n').forEach(function (line) {
      line = line.trim();
      if (!line) { return; }
      var p = line.split('|');
      if (p.length < 3) { return; }
      var type = p[0].trim().toUpperCase();
      if (type === 'SC') {
        var answers = [];
        for (var i = 2; i < p.length; i++) {
          var a = p[i].trim();
          var correct = (a.slice(-1) === '*');
          if (correct) { a = a.slice(0, -1).trim(); }
          answers.push({ text: a, correct: correct });
        }
        // Fallback: kein * vorhanden → erste Antwort gilt als richtig
        var hasCorrect = answers.some(function (a) { return a.correct; });
        if (!hasCorrect && answers.length > 0) { answers[0].correct = true; }
        qs.push({ type: 'SC', question: p[1].trim(), answers: answers });
      } else if (type === 'TF') {
        qs.push({ type: 'TF', question: p[1].trim(), tfAnswer: p[2].trim().toLowerCase() });
      } else if (type === 'KA') {
        qs.push({ type: 'KA', question: p[1].trim(), shortAnswer: p[2].trim() });
      }
    });
    return qs;
  };

  // ── Spielablauf ───────────────────────────────────────────────────────────

  RocketPickPic.prototype._showQuestion = function () {
    if (this.gameOver) { return; }
    if (this.questionIndex >= this.questions.length) { this._endGame(false); return; }
    var q = this.questions[this.questionIndex];
    this.$feedback.text('').attr('class', 'rpp-feedback');
    this.$qtext.text(q.question);
    this.$opts.empty();
    this.$btn.prop('disabled', false).show();
    if      (q.type === 'SC') { this._renderSC(q); }
    else if (q.type === 'TF') { this._renderTF(q); }
    else if (q.type === 'KA') { this._renderKA(q); }
  };

  RocketPickPic.prototype._checkAnswer = function () {
    if (this.gameOver) { return; }
    var self = this;
    var q    = this.questions[this.questionIndex];
    var correct = false, userAnswer = '';

    if (q.type === 'SC') {
      var $sel = this.$opts.find('input[type="radio"]:checked');
      if (!$sel.length) { return; }
      correct    = ($sel.closest('.rpp-option').data('correct') === true);
      userAnswer = $sel.closest('.rpp-option').find('span').text();
    } else if (q.type === 'TF') {
      var $tf = this.$opts.find('input[type="radio"]:checked');
      if (!$tf.length) { return; }
      correct    = ($tf.val() === q.tfAnswer);
      userAnswer = ($tf.val() === 'wahr') ? this.params.l10n.trueLabel : this.params.l10n.falseLabel;
    } else if (q.type === 'KA') {
      var val = this.$opts.find('.rpp-short-input').val().trim();
      if (!val) { return; }
      correct    = (val.toLowerCase() === (q.shortAnswer || '').trim().toLowerCase());
      userAnswer = val;
    }

    this.seenItems.push({ question: q, userAnswer: userAnswer, wasCorrect: correct });
    this.$btn.prop('disabled', true);
    this.questionIndex++;

    if (correct) {
      this.correctCount++;
      this._revealPart();
      this._updateProgress();
      var fb = (this.correctCount >= 15)
        ? this.params.l10n.lastCorrectFeedback
        : this.correctPool[Math.floor(Math.random() * this.correctPool.length)];
      this.$feedback.text(fb).addClass('rpp-correct');
      if (this.correctCount >= 15) {
        setTimeout(function () { self._endGame(true); }, 2000);
        return;
      }
    } else {
      var msg = this.wrongPool[Math.floor(Math.random() * this.wrongPool.length)];
      this.$feedback.text(msg).addClass('rpp-wrong');
    }
    setTimeout(function () { self._showQuestion(); }, 1400);
  };

  RocketPickPic.prototype._revealPart = function () {
    var partId = this.currentGraphic.parts[this.correctCount - 1];
    if (partId) { this.$svg.find('#' + partId).addClass('rpp-visible'); }
  };

  RocketPickPic.prototype._updateProgress = function () {
    var pct = Math.round((this.correctCount / 15) * 100);
    this.$progFill.css('width', pct + '%');
    this.$progTxt.text(this.correctCount + ' / 15');
  };

  // ── Spielende ─────────────────────────────────────────────────────────────

  RocketPickPic.prototype._endGame = function (success) {
    this.gameOver = true;
    this.$btn.hide();
    this.$opts.empty();
    this.$feedback.text('').attr('class', 'rpp-feedback');

    if (success) {
      this.$qtext.text(this.params.l10n.successMessage);
      this.$svgWrap.addClass(this.currentGraphic.animClass);
    } else {
      this.$qtext.text(this.params.l10n.poolExhaustedMessage);
    }

    this._triggerXAPI(success);
    this._renderEndButtons();
  };

  RocketPickPic.prototype._renderEndButtons = function () {
    var self = this;
    var $col = this.$container.find('.rpp-question-col');
    $col.find('.rpp-end-actions').remove();
    var $actions = $('<div class="rpp-end-actions"></div>');

    if (this.params.showRetryButton) {
      $('<button class="rpp-retry-btn"></button>').text(this.params.l10n.retryButton)
        .on('click', function () { self._retry(); }).appendTo($actions);
    }
    if (this.params.showSolutionButton && this.seenItems.length) {
      $('<button class="rpp-solution-btn"></button>').text(this.params.l10n.solutionButton)
        .on('click', function () { self._showSolution(); }).appendTo($actions);
    }
    if ($actions.children().length) { $col.append($actions); }
  };

  RocketPickPic.prototype._retry = function () {
    // Zurück zur Grafik-Auswahl falls mehrere verfügbar
    this.attach(this.$container);
  };

  // ── Lösungsansicht ────────────────────────────────────────────────────────

  RocketPickPic.prototype._showSolution = function () {
    var self = this;
    var l10n = this.params.l10n;
    var $col = this.$container.find('.rpp-question-col');
    $col.empty();

    $('<div class="rpp-solution-title"></div>').text(l10n.solutionTitle).appendTo($col);

    var $back = $('<div class="rpp-end-actions"></div>');
    $('<button class="rpp-back-btn"></button>').text(l10n.backButton)
      .on('click', function () {
        $col.empty();
        self.$qtext.text(self.correctCount >= 15 ? l10n.successMessage : l10n.poolExhaustedMessage);
        self._renderEndButtons();
      }).appendTo($back);
    $col.append($back);

    var $list = $('<div class="rpp-solution-list"></div>');
    this.seenItems.forEach(function (item, i) {
      var q = item.question;
      var correctText = '';
      if (q.type === 'SC') {
        correctText = (q.answers || []).filter(function (a) { return a.correct; })
          .map(function (a) { return a.text; }).join(', ');
      } else if (q.type === 'TF') {
        correctText = (q.tfAnswer === 'wahr') ? l10n.trueLabel : l10n.falseLabel;
      } else if (q.type === 'KA') {
        correctText = q.shortAnswer || '';
      }

      var $item = $('<div class="rpp-solution-item ' + (item.wasCorrect ? 'rpp-sol-correct' : 'rpp-sol-wrong') + '"></div>');
      $('<div class="rpp-solution-question"></div>').text((i + 1) + '. ' + q.question).appendTo($item);
      if (item.wasCorrect) {
        $('<div class="rpp-solution-answer rpp-answer-correct"></div>').text('\u2713 ' + correctText).appendTo($item);
      } else {
        $('<div class="rpp-solution-answer rpp-answer-wrong"></div>')
          .append($('<span class="rpp-sol-label"></span>').text(l10n.yourAnswerLabel + ' '))
          .append($('<span></span>').text(item.userAnswer || '\u2013')).appendTo($item);
        $('<div class="rpp-solution-answer rpp-answer-correct"></div>')
          .append($('<span class="rpp-sol-label"></span>').text(l10n.correctAnswerLabel + ' '))
          .append($('<span></span>').text(correctText)).appendTo($item);
      }
      $list.append($item);
    });
    $col.append($list);
    H5P.trigger(this, 'resize');
  };

  RocketPickPic.prototype._triggerXAPI = function (passed) {
    var ev = this.createXAPIEventTemplate('completed');
    ev.setScoredResult(this.getScore(), this.getMaxScore(), this, true, passed);
    this.trigger(ev);
  };

  // ── Antwort-Rendering ─────────────────────────────────────────────────────

  RocketPickPic.prototype._renderSC = function (q) {
    var self = this;
    var shuffled = (q.answers || []).slice();
    this._shuffle(shuffled);
    shuffled.forEach(function (ans) {
      var $lbl = $('<label class="rpp-option"></label>');
      $lbl.data('correct', ans.correct === true);
      $lbl.append($('<input type="radio" name="rpp-sc">'));
      $lbl.append($('<span></span>').text(ans.text));
      self.$opts.append($lbl);
    });
  };

  RocketPickPic.prototype._renderTF = function (q) {
    var self = this;
    var l10n = this.params.l10n;
    [{ val: 'wahr', label: l10n.trueLabel }, { val: 'falsch', label: l10n.falseLabel }]
      .forEach(function (opt) {
        var $lbl = $('<label class="rpp-option"></label>');
        $lbl.append($('<input type="radio" name="rpp-tf">').val(opt.val));
        $lbl.append($('<span></span>').text(opt.label));
        self.$opts.append($lbl);
      });
  };

  RocketPickPic.prototype._renderKA = function (q) {
    var $inp = $('<input type="text" class="rpp-short-input">')
      .attr('placeholder', this.params.l10n.answerPlaceholder)
      .attr('autocomplete', 'off');
    this.$opts.append($inp);
    setTimeout(function () { $inp.focus(); }, 60);
  };

  // ── DOM-Aufbau ────────────────────────────────────────────────────────────

  RocketPickPic.prototype._buildShell = function () {
    return (
      '<div class="rpp-graphic-col">' +
        '<div class="rpp-svg-wrap">' + this.currentGraphic.buildSVG() + '</div>' +
        '<div class="rpp-progress"><div class="rpp-progress-fill"></div></div>' +
        '<div class="rpp-progress-text">0 / 15</div>' +
      '</div>' +
      '<div class="rpp-question-col">' +
        '<div class="rpp-question-text"></div>' +
        '<div class="rpp-answer-options"></div>' +
        '<button class="rpp-submit">' + this._esc(this.params.l10n.submitButton) + '</button>' +
        '<div class="rpp-feedback"></div>' +
      '</div>'
    );
  };

  // ── Hilfsfunktionen ───────────────────────────────────────────────────────

  RocketPickPic.prototype._shuffle = function (arr) {
    for (var i = arr.length - 1; i > 0; i--) {
      var j = Math.floor(Math.random() * (i + 1));
      var t = arr[i]; arr[i] = arr[j]; arr[j] = t;
    }
  };

  RocketPickPic.prototype._esc = function (str) {
    return String(str).replace(/&/g, '&amp;').replace(/</g, '&lt;')
      .replace(/>/g, '&gt;').replace(/"/g, '&quot;');
  };

  return RocketPickPic;

